# 3. Cost-Benefit Function Pi(T)
# ---------------------------------------------------------------------------

def plot_cost_benefit():
    r"""
    Line plot: Cost-Benefit function $\Pi(T)$ over time.

    Model:
        Investment cost  C(T) = C_0 * exp(-alpha * T)
        Cumulative benefit  B(T) = B_max * (1 - exp(-gamma * T))
        Net benefit  Pi(T) = B(T) - C(T)

    Three operator profiles are plotted to show how CRC/CMF affect the
    cost-benefit trajectory.
    """
    T = np.linspace(0, 36, 200)

    # Advanced operator: high benefit rate, fast cost recovery
    C_0_adv, alpha_adv, B_max_adv, gamma_adv = 50.0, 0.12, 200.0, 0.15
    C_adv = C_0_adv * np.exp(-alpha_adv * T)
    B_adv = B_max_adv * (1 - np.exp(-gamma_adv * T))
    Pi_adv = B_adv - C_adv

    # Mid-tier operator
    C_0_mid, alpha_mid, B_max_mid, gamma_mid = 50.0, 0.08, 150.0, 0.10
    C_mid = C_0_mid * np.exp(-alpha_mid * T)
    B_mid = B_max_mid * (1 - np.exp(-gamma_mid * T))
    Pi_mid = B_mid - C_mid

    # Lagging operator
    C_0_lag, alpha_lag, B_max_lag, gamma_lag = 50.0, 0.05, 80.0, 0.06
    C_lag = C_0_lag * np.exp(-alpha_lag * T)
    B_lag = B_max_lag * (1 - np.exp(-gamma_lag * T))
    Pi_lag = B_lag - C_lag

    fig, ax = plt.subplots(figsize=(10, 5.5))

    ax.plot(T, Pi_adv, color=COLORS["Advanced"], linewidth=2.2,
            label="Advanced (high CRC/CMF)")
    ax.plot(T, Pi_mid, color=COLORS["Mid-tier"], linewidth=2.2,
            label="Mid-tier (medium CRC/CMF)")
    ax.plot(T, Pi_lag, color=COLORS["Lagging"], linewidth=2.2,
            label="Lagging (low CRC/CMF)")

    # Zero line
    ax.axhline(y=0, color="black", linewidth=0.8, linestyle="-")

    # Shade positive region
    ax.fill_between(T, 0, Pi_adv, where=(Pi_adv > 0), alpha=0.06,
                   color=COLORS["Advanced"])
    ax.fill_between(T, 0, Pi_mid, where=(Pi_mid > 0), alpha=0.06,
                   color=COLORS["Mid-tier"])

    # Break-even annotations
    for Pi_arr, name, color in [(Pi_adv, "Advanced", COLORS["Advanced"]),
                                (Pi_mid, "Mid-tier", COLORS["Mid-tier"]),
                                (Pi_lag, "Lagging", COLORS["Lagging"])]:
        # Find first crossing
        crossings = np.where(np.diff(np.sign(Pi_arr)) > 0)[0]
        if len(crossings) > 0:
            idx = crossings[0]
            # Linear interpolation for break-even month
            t_be = T[idx] - Pi_arr[idx] * (T[idx + 1] - T[idx]) / (
                Pi_arr[idx + 1] - Pi_arr[idx]
            )
            ax.axvline(x=t_be, color=color, linestyle=":", alpha=0.5, linewidth=1)
            ax.annotate(f"BE≈{t_be:.1f}m", xy=(t_be, 0),
                        xytext=(t_be + 0.5, -30), fontsize=8, color=color)

    ax.set_xlabel("Time (months)", fontsize=13)
    ax.set_ylabel("Π(T) — Net Benefit (arbitrary units)", fontsize=13)
    ax.set_title("Cost-Benefit Function Π(T) Over Time", fontsize=15,
                 fontweight="bold")
    ax.legend(fontsize=11, loc="upper left")
    ax.set_xlim(0, 36)
    ax.grid(True, alpha=0.35, linestyle="--")
    fig.tight_layout()

    out_path = os.path.join(FIG_DIR, "cost_benefit_pi.png")
    fig.savefig(out_path, dpi=200, bbox_inches="tight")
    plt.close(fig)
    print(f"Saved: {out_path}")


