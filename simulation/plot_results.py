# 2. DFI vs MTTR Curve
# ---------------------------------------------------------------------------

def plot_dfi_vs_mttr():
    """
    Curve plot: DFI vs MTTR relationship.

    MTTR(lambda) = MTTR_0 * exp(-lambda * DFI)
    """
    MTTR_0 = 240.0
    lam = 0.8

    dfi = np.linspace(0.0, 5.0, 200)
    mttr = MTTR_0 * np.exp(-lam * dfi)

    fig, ax = plt.subplots(figsize=(10, 5.5))
    ax.plot(dfi, mttr, color="#2ca02c", linewidth=2.5)
    ax.fill_between(dfi, mttr, alpha=0.12, color="#2ca02c")

    # Annotate key points
    for d_val in [1.0, 2.0, 3.0, 4.0]:
        m_val = MTTR_0 * np.exp(-lam * d_val)
        ax.plot(d_val, m_val, "ko", markersize=5)
        ax.annotate(f"DFI={d_val:.0f}, MTTR={m_val:.1f}min",
                    xy=(d_val, m_val), xytext=(d_val + 0.25, m_val + 12),
                    fontsize=9, arrowprops=dict(arrowstyle="->", color="grey"))

    ax.set_xlabel("DFI (Digital Fitness Index)", fontsize=13)
    ax.set_ylabel("MTTR (minutes)", fontsize=13)
    ax.set_title("DFI vs MTTR Relationship (λ = 0.8)", fontsize=15,
                 fontweight="bold")
    ax.grid(True, alpha=0.35, linestyle="--")
    ax.set_xlim(0, 5)
    ax.set_ylim(0, MTTR_0 * 1.05)
    fig.tight_layout()

    out_path = os.path.join(FIG_DIR, "dfi_vs_mttr.png")
    fig.savefig(out_path, dpi=200, bbox_inches="tight")
    plt.close(fig)
    print(f"Saved: {out_path}")


