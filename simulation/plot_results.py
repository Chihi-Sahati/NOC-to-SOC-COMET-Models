# 1. MCI Evolution Plot
# ---------------------------------------------------------------------------

def plot_mci_evolution():
    """
    Line plot: MCI over 36 months for Advanced, Mid-tier, and Lagging operators.

    Requires simulation_results.npz generated by scenario_runner.py.
    If the file is missing, the data is computed inline.
    """
    WEIGHTS = np.array([0.18, 0.16, 0.15, 0.14, 0.14, 0.12, 0.11])
    path = os.path.join(SIM_DIR, "simulation_results.npz")
    if os.path.exists(path):
        data = np.load(path, allow_pickle=True)
        months = data["months"]
        adv_traj = data["advanced_trajectory"]
        mid_traj = data["midtier_trajectory"]
        lag_traj = data["lagging_trajectory"]
    else:
        # Fallback: generate data inline
        np.random.seed(42)
        months = np.arange(37)
        adv_traj = np.zeros((37, 7))
        mid_traj = np.zeros((37, 7))
        lag_traj = np.zeros((37, 7))
        adv_traj[0] = np.array([4.2, 4.0, 4.5, 3.8, 4.1, 4.3, 4.4])
        mid_traj[0] = np.array([2.8, 2.5, 2.3, 2.1, 2.7, 2.9, 3.0])
        lag_traj[0] = np.array([1.8, 1.5, 1.2, 1.0, 1.6, 1.9, 2.0])
        for t in range(1, 37):
            for arr, drift in [(adv_traj, 0.01), (mid_traj, 0.008), (lag_traj, 0.005)]:
                arr[t] = arr[t - 1] + np.random.normal(drift, 0.05, 7)
                arr[t] = np.clip(arr[t], 0, 5)

    mci_adv = np.array([np.dot(adv_traj[t], WEIGHTS) for t in range(len(months))])
    mci_mid = np.array([np.dot(mid_traj[t], WEIGHTS) for t in range(len(months))])
    mci_lag = np.array([np.dot(lag_traj[t], WEIGHTS) for t in range(len(months))])

    fig, ax = plt.subplots(figsize=(10, 5.5))
    ax.plot(months, mci_adv, color=COLORS["Advanced"], linewidth=2.2,
            label="Advanced (HKT/Orange)", marker="o", markersize=3)
    ax.plot(months, mci_mid, color=COLORS["Mid-tier"], linewidth=2.2,
            label="Mid-tier", marker="s", markersize=3)
    ax.plot(months, mci_lag, color=COLORS["Lagging"], linewidth=2.2,
            label="Lagging", marker="^", markersize=3)

    ax.set_xlabel("Month", fontsize=13)
    ax.set_ylabel("MCI (Maturity Composite Index)", fontsize=13)
    ax.set_title("MCI Evolution Over 36 Months by Operator Type", fontsize=15,
                 fontweight="bold")
    ax.legend(fontsize=11, loc="lower right")
    ax.set_xlim(0, 36)
    ax.set_ylim(0, 5)
    ax.grid(True, alpha=0.35, linestyle="--")
    fig.tight_layout()

    out_path = os.path.join(FIG_DIR, "mci_evolution.png")
    fig.savefig(out_path, dpi=200, bbox_inches="tight")
    plt.close(fig)
    print(f"Saved: {out_path}")


